package com.cg.session.exception;

public class ScheduledSessionException extends Exception {
	private static final long serialVersionUID = 4108432909961634888L;

	public ScheduledSessionException() {
		super();
		
	}

	public ScheduledSessionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public ScheduledSessionException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ScheduledSessionException(String message) {
		super(message);
		
	}

	public ScheduledSessionException(Throwable cause) {
		super(cause);
		
	}

	
}
